package veryEasy;

public class Test022EqualOrSameToZero {

	// Create a method that takes an integer as its only argument and returns true
	// if it's less than or equal to zero, otherwise return false.

	public static boolean lessThanOrEqualToZero(int num) {

		if (num <= 0) {
			return true;
		} else {
			return false;
		}

	}

}

package veryEasy;

public class Test029FlipTheBool {

	// Make a function that returns an int boolean opposite of an int boolean given
	// (flip the int boolean).

	public static int flipIntBool(int intBool) {

		if (intBool == 0) {
			return intBool = 1;
		} else {
			return intBool = 0;
		}

	}
}
